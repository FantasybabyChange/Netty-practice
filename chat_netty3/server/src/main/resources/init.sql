CREATE DATABASE netty_test;
USE  netty_test;

CREATE TABLE player(
	playerId BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '玩家ID',
	playerName INT UNSIGNED NOT NULL DEFAULT '0' COMMENT '应用id',
	PASSWORD VARCHAR(20) NOT NULL DEFAULT '' COMMENT '密码',
	`level` INT UNSIGNED NOT NULL DEFAULT '0' COMMENT '等级',
	EXP INT UNSIGNED NOT NULL DEFAULT '0' COMMENT '展示数',
	PRIMARY KEY (playerId)
	)ENGINE=INNODB DEFAULT CHARSET=utf8
